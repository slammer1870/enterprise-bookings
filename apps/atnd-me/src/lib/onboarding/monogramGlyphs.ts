const GLYPH_COLS = 5
const GLYPH_ROWS = 7

/**
 * Render monogram characters as SVG rects (font-independent for sharp/librsvg).
 */
export function monogramGlyphsToSvgPaths(
  monogram: string,
  size: number,
  fill: string,
): string {
  const chars = monogram
    .toUpperCase()
    .split('')
    .filter((ch) => Boolean(GLYPHS[ch]))
  const glyphs = (chars.length > 0 ? chars : ['?']).slice(0, 2).map((ch) => GLYPHS[ch]!)

  const gap = size * 0.04
  const padding = size * 0.18
  const usable = size - padding * 2
  const glyphCount = glyphs.length
  const totalGap = gap * (glyphCount - 1)
  const glyphWidth = (usable - totalGap) / glyphCount
  const cell = Math.min(glyphWidth / GLYPH_COLS, usable / GLYPH_ROWS)
  const glyphPixelW = cell * GLYPH_COLS
  const glyphPixelH = cell * GLYPH_ROWS
  const blockWidth = glyphCount * glyphPixelW + totalGap
  const startX = (size - blockWidth) / 2
  const startY = (size - glyphPixelH) / 2

  const rects: string[] = []
  glyphs.forEach((rows, gi) => {
    const ox = startX + gi * (glyphPixelW + gap)
    rows.forEach((row, ry) => {
      for (let cx = 0; cx < row.length; cx++) {
        if (row[cx] !== '1') continue
        const x = ox + cx * cell
        const y = startY + ry * cell
        // Slight inset keeps cells from looking fused when scaled.
        const inset = cell * 0.08
        rects.push(
          `<rect x="${(x + inset).toFixed(2)}" y="${(y + inset).toFixed(2)}" width="${(cell - inset * 2).toFixed(2)}" height="${(cell - inset * 2).toFixed(2)}" rx="${(cell * 0.12).toFixed(2)}" fill="${fill}"/>`,
        )
      }
    })
  })

  return rects.join('\n  ')
}
